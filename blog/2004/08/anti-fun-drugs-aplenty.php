     <font class="TallRed">Anti-Fun Drugs Aplenty<a name="109246537518887708">&nbsp;</a></font>
		<div class="blackjustified" style="background-color:#eeeeee;padding:5px 5px 5px 5px;border-top:3px solid #cccccc;border-bottom:1px solid #333333;border-left:1px solid #cccccc;border-right:1px solid #cccccc;">
          Well, what drugs do I have right now? Not much...<br /><br />The most interesting drugs I have are really anti-drugs. For example, I have a fuckload (that's a technical term) of anti-psychotics, strong enough to turn me from a semi-hallucinating paranoid maniac into an empty shell of a person.<br /><br />Antipsychotics, however, are very very VERY useful in one specific case: Bad trips.<br /><br />Suppose one of my friends was tripping on LSD, and suppose it went bad. Now, I've been there before, and trust me there is very little in this world worse than a bad acid trip. Enter anti-psychotics. Assuming they want the trip to end, under normal circumstances they're simply fucked over until several hours (physical time; percieved time for the tripper is probably going to be on the order of 12 to 48 hours) has passed. Antipsychotics will end their trip, and end it NOW.<br /><br />Got a bad trip? Snort yourself 5mg of olanzapine, and within 5 to 10 minutes, you will be down, and no amount of LSD is gonna get you back up for at least 36 hours. What use is olanzapine? It's a first-aid medicine for the psychonaught. It's a security blanket. Olanzapine is being able to know that if it gets bad, you can end it before it gets worse. Honestly I'm surprised you don't see antipsychotics on the black market.<br /><br />In addition to anti-psychotics, I also have a bunch of anti-depressants (Wellbutrin). Of course, wellbutrin isn't nearly as useful  as olanzapine to the psychonaught; All wellbutrin will do is forcibly regulate the serotonin release and reception processes in your brain. Wellbutrin will make ecstacy behave like little more than a sugar pill, but honestly I can't see why anybody would want to kill their ecstacy experience. I suppose one scenario is "Oh shit I'm rolling and my parents just got home...", but I don't think that that's all that likely.<br /><br />So, yeah, what is my anti-drug? My "anti-drug" is a combo of pharmaceuticals tailored to anti-fuck your mind. I take a steady dose of 500 to 600mg per day of seroquel, another antipsychotic I was switched to after olanzapine simply stopped working effectively on me, and I also take 150mg per day of wellbutrin. So, even if I wanted to do them, LSD, mushrooms, ecstacy, mescaline, and any other dopaminic or serotonergic (spelling? who knows...) drug will simply have no effect on me.<br /><br />My anti-drug, then, I suppose, is drugs that keep me forcibly sane. Hehe...<br /><br />Mind you, I've found several interesting ways of getting fucked up despite my medications, but those I will save for another post. Happy tripping, class, and don't forget that you can always contact DrRobin if things get messy. :D<br />
