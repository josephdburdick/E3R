     <font class="TallRed">Chicken is the enemy <a name="10927508968860460">&nbsp;</a></font>
		<div class="blackjustified" style="background-color:#eeeeee;padding:5px 5px 5px 5px;border-top:3px solid #cccccc;border-bottom:1px solid #333333;border-left:1px solid #cccccc;border-right:1px solid #cccccc;">
          Laugh if you must but chicken IS the enemy and today is the day that you are going to know why.<br /><br />"Virginia physician and scientist Richard Atkinson, who is president of the American Obesity Association, has set up a new lab in Richmond called Obetech designed to test human blood for a fat virus.  Before you laugh, know this: There is such a virus in chickens. And weirdly, the chickens that are infected are much fatter but have lower blood cholesterol and triglyceride levels than chickens that are not infected. Atkinson and Nikhal Dhurandhar have identified a similar "fat virus" that they say is "quite common in humans." It's called adenovirus (AD) 36. Atkinson's lab test costs between $125 to $150 and results take a few weeks."<br /><br />And for those waste of time diets:<br /><br />'Even if our weight gain is caused by a fat virus, eating less and moving more are still the best way to fight obesity. But understanding why we get fat, why some people can carry the extra weight with minimal health impact, and how to predict which slender twentysomethings will have a beer belly by age 45 are questions that intrigue scientists. <br /><br />Atkinson and Dhurandhar injected the AD 36 fat virus into healthy animals and noticed it caused them to become fat compared with control groups that did not receive the injection. Washington Post reporter Sally Squires writes that just like Dhurandhar's chickens, the infected animals had blood cholesterol and triglyceride levels that remained lower than those in uninfected animals.<br /><br />Since they can't inject a virus into human beings, the team screened blood from obese Americans and people of a healthy weight. They specifically looked for a previous AD 36 infection by hunting for antibodies against the fat virus. They found that about 30 percent of the obese participants had antibodies against AD 36, compared with 10 percent of the healthy-weight control subjects. Once again, those who had AD 36 also had significantly lower blood cholesterol and triglyceride levels than their uninfected counterparts.'<br /><br /><br />Huh, who would of thought chicken IS the enemy?!  At any rate, Think again when you go to Chick-fil-A and order those chicken fingers.  That whole empire is out to take your money, make you fat, and do it all over.  <br /><br /><br />
