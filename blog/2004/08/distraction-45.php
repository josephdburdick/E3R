          Things seem rather quiet here lately.  Allow me to interrupt the silence with a few tastelessly decorated quacks myself.<br /><br />These are the last weeks of summer for me, and ironically they feel exactly like the first.  No more obligations.  Work: over.  Vacation:  done.  Settlement:  very damn near to being behind me for good. <br /><br />So i wake up earlier than normal,  a result of the jet lag that still lingers even a week after hitting US soil.  Probably for the best anyway, i sleep like a vampire, practically.  However the obstacle i face each day is how to make my day as constructive as possible.  The thought of being absolutely idle makes my stomach turn.  There are three defining points to my day:  the starting point, where i force myself out of bed and into the shower,  the climax point, where i feel the neccessity to get out and venture into the mad social life that beacons on a day to day basis, and the final point, that point of falling action where i drop my body into the comfort of my bed and let sleep whisk me away.   The interval between points one and two is where i find myself looking for things to keep myself busy, always.  So i distract myself with movies (something i find far more constructive than stalenesss of television), guitar, and books.  And i avoid writing; something i enjoy doing, but can't find myself doing unless i force myself to.  Is this how it works?  I'm willing to bet all good writers can hardly pry themselves away from the typewriter.  Maybe i need to get out more.  And yet i just toured half of Italy for God's sake.  If the waters of Venice can't draw words out of me, what will?  Even in writing this utterly vain paragraph i find myself struggling.  And why should i?  This is only just another daily distraction.  <br /><br />And a total break from face character, at that. <br /><br />Adios<br />
