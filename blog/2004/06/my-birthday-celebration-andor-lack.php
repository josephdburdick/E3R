          <img src="http://www.thebroadwaycollection.com/images/shows/12/showpic_12.jpg" hspace="3" align="right">Well, it's Sunday and boy am I ready for some hardcore sleepin' action. My birthday was on Friday and Morg and I went to see STOMP at Richmond's Landmark Theatre. It was totally awesome. The beats were right up my alley. It was like...industrial techno without the computers. When Morgan asked me how I liked it my official response was "I have never been so entertained in my life." Very cool show, if any of you have any extra cash at the given time that STOMP is performing in your area, go check it out. <br /><br />In other entertaining news, none of my family members called me on my birthday. I found this so funny that I decided to start calling <i>them</i>. All I got was voicemail and even then, I never got a response until the next day. <i>Glad to see you people are on top of things, thanks!</i> I didn't even want anything except for a "happy birthday" from the people who were responsible for it. But, no deal. My mom said, "you know me, son. I was too busy, sorry!" and my dad said, "I was going to call you...but I fell asleep instead." My uncle, the one that I'm staying with right now for this internship had no clue that my birthday was on Friday and was still confused as to which day it was on until I came back tonight and cleared it up for him. I had to use my own money for dinner (except for what morgan bought me in pity [thanks morgan!] and now the money that I spent I owe to <i>myself</i> due to lack of funds and the required money for costly gas and metro tickets. <br /><br />After the show, we picked up some <a href="http://www.blogger.com/profile/3586520">friends</a> around the area and would have had more but a certain <a href="http://www.blogger.com/profile/3571803">two</a> <a href="http://www.blogger.com/profile/3571753">significants</a> weren't in town. We went to Denny's for dinner which was fine with me and soon called it a night. Morgan was sick most of the weekend so she wasn't having as much fun as she could have had but went with the flow of things anyway. <blockquote><b>Things that must be frowned upon this weekend:</b> parking tickets, gas prices, friends who call you when you're in town just to get a ride to some place. All equally annoying. And if you reading this and you're responsible for any of these things, go pat yourself on the back, ass.<br /><b>Things that must be smiled upon this weekend:</b> families that enjoy you more than your own, playing guitar with skilled drummer, eating enough 'complimentary birthday dessert dishes' to make Dr. Phil go into a seizure.</blockquote> By the way, when I'm talking about "family" I'm talkin intermediate family not my ultra-fabulous-adopted-aunt/uncle family. Confused? Get used to it, and no, I don't live in West Virginia. <br />
