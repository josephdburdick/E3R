          That's some funny shit.  I have to laugh at stuff like that, I can't help myself.  Sure, it looks cool, but I don't see a bunch of guys flipping around.  I see a desperate attempt to say "See? I told you gymnastics isn't gay!"<br /><br />It also reminds me of Wesley Snipes, the dancer that became an ass kicker in genre-twisting films.  Is it an action movie?  Is it a mystery?  No, it's Wesley Snipes being cool.  And if you think about it, that stuff completely lacks martial effectiveness.  The special thing about Wesley Snipes action movies is the moment of pause the bad guy takes after being hit, handy for setting up a gradiose combination.  Jean Claude's movies have that same pause, but done up with the melodramatic slow mo.  Bloodsport has got to be one of the greatest B comedy movies of all time.  Makes me think of Jet Li, action film Gung Fu hero who's just a Wu Shu (a.k.a Souless Gung Fu) expert.  I generally like Jet Li's stuff, he's fun to watch.  But at the same time, I know three guys that could show him just how little Gung Fu he has.  That's basically my whole point, flipping and spinning isn't Gung Fu.<br /><br />I usually end up giggling at action movies, either because of the ridiculous fight choreography or the bad storyline.  Action (and Gung Fu) movies are actually a lot like porn if you think about it.  Some generic, predictable story line that's only there to tie together the hardcore action.  I'd love to see someone do Gung Fu (I spell it that way because I prefer a different transliteration system than Wade-Giles, the commonly used one) films that handle the combat like an art film does tasteful nudity.  Lots of nakedness, very erotic stuff, but generally realistic and wholly complimentary to the story.  The story is great, and the nakedness is even better.  If a Gung Fu movie was done that way, it'd be groundbreaking.  It has been tried before, but always with that Bruce Lee tinge to it.  Bruce Lee movies (while enjoyable) are Gung Fu-sploitation, not to be taken as seriously in our own time as what I propose.  FYI, I differentiate a Gung Fu movie from an action movie because it focuses on exploration of a martial style rather than just combat.<br /><br />Don't bother responding saying that I missed the point.  I didn't miss it at all, that was very enjoyable stuff.  Like any geek worth his tech library, I just think too much; that's all.<br /><br /><br />P.S.  I never have figured out when to use semi-colons in writing.  I like commas and parantheses, as you all no doubt know by now.<br />
